x = bool(input('Digite um valor para X ou aperte enter: '))
y = bool(input('Digite um valor para Y ou aperte enter: '))

if x == y:
    print(f'X é {x}, Y é {y}')
elif x != y:
    print(f'X é {x}, Y é {y}')
else:
    print(f'X é {x}, Y é {y}')

