numInt = int(input('Insira um número de 1 a 7: '))

if numInt == 1:
    print('Domingo')
elif numInt == 2:
    print('Segunda-Feira')
elif numInt == 3:
    print('Terça-Feira')
elif numInt == 4:
    print("Quarta-Feira")
elif numInt == 5:
    print('Quinta-Feira')
elif numInt == 6:
    print('Sexta-Feira')
elif numInt == 7:
    print('Sábado')
else:
    print('Não existe dia da semana com esse numero!')




